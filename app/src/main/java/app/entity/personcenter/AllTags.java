package app.entity.personcenter;

import app.entity.content.Tags;

import java.util.List;

public class AllTags {
    private List<Tags> choose;
    private List<Tags> tags;

    public List<Tags> getChoose() {
        return choose;
    }

    public void setChoose(List<Tags> choose) {
        this.choose = choose;
    }

    public List<Tags> getTags() {
        return tags;
    }

    public void setTags(List<Tags> tags) {
        this.tags = tags;
    }

    @Override
    public String toString() {
        return "AllTags{" +
                "choose="+choose+
                ",tags="+tags+
                "}";
    }
}
